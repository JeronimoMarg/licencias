public void clearTable(){
        DefaultTableModel tableModel = (DefaultTableModel) tablaLicencias.getModel();
        tableModel.setRowCount(0);
    }

public void cargarTabla(LicenciaDTO licencia){
        DefaultTableModel tableModel = (DefaultTableModel) tablaLicencias.getModel();
        Object[] dataRow = {licencia.getNumeroLicencia(),licencia.getNombreTitular(),licencia.getApellidoTitular(),licencia.getTipoLicencia().getAtributo1(),licencia.getInicioVigencia(),licencia.getFinVigencia()};
        tableModel.addRow(dataRow);
    }

